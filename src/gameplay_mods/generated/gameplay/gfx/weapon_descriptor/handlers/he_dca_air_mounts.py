"""Auto-wire ``Ammo_<x>_AIR`` mounts on every turret carrying a ``DamageFamily_he_dca`` ammo.

Runs as the final step of ``edit_gen_gp_gfx_weapondescriptor`` (after vanilla
renames, new units, unit edits, default salves, and namespace ammo quantity
updates) so the live NDF reflects every prior change. Skips weapons excluded
from the precomputed ``he_dca_weapons`` map (air-only DCA with
``MaximumRangeGRU == 0``). For each ``TMountedWeaponDescriptor`` whose
``Ammunition`` points at a SPAAG ammo covered by that map, we clone the mount
and swap the clone's ``Ammunition`` pointer to the matching ``_AIR`` descriptor
(generated by ``apply_he_dca_air_ammo_clones``).

Each AIR mount gets its own ``WeaponShootData`` / Active / Ignored channel so
DepictionVehicles can attach a separate fire operator (high-RoF ContinuousFire
breaks when ground + AIR share one channel). ``AmmoBoxIndex`` and
``HandheldEquipmentKey`` stay shared with the ground donor.

Channel assignments are recorded on ``game_db["he_dca_air_depiction_channels"]``
for the DepictionVehicles pass.

Idempotent: a re-run skips cloning when the ``_AIR`` sibling is already
present; existing AIR mounts that still share the ground shoot channel are
rewired, and depiction metadata is always (re)populated.
"""

import re
from typing import Any, Dict

from src.utils.ndf_utils import is_obj_type, is_valid_turret, strip_quotes


_SALVO_SUFFIX_RE = re.compile(r"(_x\d+|_salvolength\d+|_infmagazine\d+)$")
_AMMO_PREFIX = "$/GFX/Weapon/Ammo_"
_SHOOT_INDEX_RE = re.compile(r"WeaponShootData_\d+_(\d+)")
_ACTIVE_INDEX_RE = re.compile(r"WeaponActiveAndCanShoot_(\d+)")
_WD_PREFIX = "WeaponDescriptor_"

_CHANNELS_KEY = "he_dca_air_depiction_channels"


def _extract_ammo_namespace(weapon: Any) -> str | None:
    """Return the ammo namespace (without ``Ammo_`` prefix) or ``None``."""
    ammunition_membr = weapon.v.by_m("Ammunition", False)
    if ammunition_membr is None:
        return None
    raw = str(ammunition_membr.v)
    if _AMMO_PREFIX not in raw:
        return None
    return raw.split(_AMMO_PREFIX, 1)[1].strip()


def _air_namespace(ammo: str) -> str:
    """Append ``_AIR`` to the post-``Ammo_`` namespace, preserving any salvo suffix.

    Matches the cloning rule in ``apply_he_dca_air_ammo_clones`` so the
    swapped pointer always resolves to a real descriptor.
    """
    return f"{ammo}_AIR"


def _unit_name_from_weapon_descr(weapon_descr: Any) -> str | None:
    ns = getattr(weapon_descr, "namespace", None)
    if not ns or not str(ns).startswith(_WD_PREFIX):
        return None
    return str(ns)[len(_WD_PREFIX) :]


def _first_shoot_name(mount: Any) -> str | None:
    """Return the first ``WeaponShootData_*_*`` name on a mount, or ``None``."""
    shoot_membr = mount.v.by_m("WeaponShootDataPropertyName", False)
    if shoot_membr is None:
        return None
    match = _SHOOT_INDEX_RE.search(str(shoot_membr.v))
    if not match:
        return None
    # Reconstruct the full token from the match span
    return match.group(0)


def _channel_indices_on_mount(mount: Any) -> list[int]:
    indices: list[int] = []
    shoot_membr = mount.v.by_m("WeaponShootDataPropertyName", False)
    if shoot_membr is not None:
        indices.extend(int(m) for m in _SHOOT_INDEX_RE.findall(str(shoot_membr.v)))
    active_membr = mount.v.by_m("WeaponActiveAndCanShootPropertyName", False)
    if active_membr is not None:
        active = strip_quotes(str(active_membr.v))
        match = _ACTIVE_INDEX_RE.fullmatch(active)
        if match:
            indices.append(int(match.group(1)))
    return indices


def _max_channel_index(weapon_descr: Any) -> int:
    """Highest shoot/active channel index used anywhere on this descriptor."""
    max_n = 0
    turret_list_membr = weapon_descr.v.by_m("TurretDescriptorList", False)
    if turret_list_membr is None:
        return max_n
    for turret in turret_list_membr.v:
        if not is_valid_turret(turret.v):
            continue
        mounted_list_membr = turret.v.by_m("MountedWeaponDescriptorList", False)
        if mounted_list_membr is None:
            continue
        for mount in mounted_list_membr.v:
            if not is_obj_type(mount.v, "TMountedWeaponDescriptor"):
                continue
            for n in _channel_indices_on_mount(mount):
                if n > max_n:
                    max_n = n
    return max_n


def _assign_air_channel(air_mount: Any, channel: int, air_ns: str) -> None:
    """Set distinct shoot/active/ignored/EffectTag on an AIR mount."""
    air_mount.v.by_m("WeaponShootDataPropertyName").v = (
        f"['WeaponShootData_0_{channel}']"
    )
    active_membr = air_mount.v.by_m("WeaponActiveAndCanShootPropertyName", False)
    if active_membr is not None:
        active_membr.v = f"'WeaponActiveAndCanShoot_{channel}'"
    ignored_membr = air_mount.v.by_m("WeaponIgnoredPropertyName", False)
    if ignored_membr is not None:
        ignored_membr.v = f"'WeaponIgnored_{channel}'"
    effect_membr = air_mount.v.by_m("EffectTag", False)
    if effect_membr is not None:
        effect_membr.v = f"'FireEffect_{air_ns}'"


def _record_channel(
    channels_by_unit: Dict[str, list],
    unit_name: str,
    ground_shoot: str,
    channel: int,
    ground_ammo_ns: str,
) -> None:
    channels_by_unit.setdefault(unit_name, []).append(
        {
            "ground_shoot": ground_shoot,
            "air_shoot": f"WeaponShootData_0_{channel}",
            "air_active": f"WeaponActiveAndCanShoot_{channel}",
            "ground_ammo_ns": ground_ammo_ns,
        }
    )


def apply_he_dca_air_mounts(
    source_path: Any,
    logger: Any,
    game_db: Dict[str, Any],
) -> None:
    """Add an air-targeting mount to every turret that carries a ``DamageFamily_he_dca`` ammo."""
    he_dca_weapons = game_db.get("ammunition", {}).get("he_dca_weapons", {})
    if not he_dca_weapons:
        logger.info(
            "(he_dca_air) precomputed he_dca_weapons map is empty; "
            "no air mounts will be added"
        )
        return

    channels_by_unit: Dict[str, list] = game_db.setdefault(_CHANNELS_KEY, {})
    # Fresh pass each run so depiction metadata matches live NDF.
    channels_by_unit.clear()

    added = 0
    rewired = 0
    skipped_ok = 0

    for weapon_descr in source_path:
        unit_name = _unit_name_from_weapon_descr(weapon_descr)
        turret_list_membr = weapon_descr.v.by_m("TurretDescriptorList", False)
        if turret_list_membr is None:
            continue

        next_channel = _max_channel_index(weapon_descr)

        for turret in turret_list_membr.v:
            if not is_valid_turret(turret.v):
                continue

            mounted_list_membr = turret.v.by_m("MountedWeaponDescriptorList", False)
            if mounted_list_membr is None:
                continue

            mounts = list(mounted_list_membr.v)
            mounts_by_ammo: Dict[str, Any] = {}
            ground_mounts = []
            for mount in mounts:
                if not is_obj_type(mount.v, "TMountedWeaponDescriptor"):
                    continue
                ammo_ns = _extract_ammo_namespace(mount)
                if ammo_ns is None:
                    continue
                mounts_by_ammo[ammo_ns] = mount
                base = _SALVO_SUFFIX_RE.sub("", ammo_ns)
                if base in he_dca_weapons and not ammo_ns.endswith("_AIR"):
                    ground_mounts.append((mount, ammo_ns))

            for ground_mount, ammo_ns in ground_mounts:
                air_ns = _air_namespace(ammo_ns)
                ground_shoot = _first_shoot_name(ground_mount)
                if ground_shoot is None:
                    logger.warning(
                        f"(he_dca_air) {weapon_descr.namespace}: ground mount "
                        f"Ammo_{ammo_ns} has no WeaponShootData; skipping AIR"
                    )
                    continue

                existing_air = mounts_by_ammo.get(air_ns)
                if existing_air is None:
                    next_channel += 1
                    new_mount = ground_mount.copy()
                    new_mount.v.by_m("Ammunition").v = f"{_AMMO_PREFIX}{air_ns}"
                    _assign_air_channel(new_mount, next_channel, air_ns)
                    mounted_list_membr.v.add(new_mount)
                    mounts_by_ammo[air_ns] = new_mount
                    added += 1
                    if unit_name:
                        _record_channel(
                            channels_by_unit,
                            unit_name,
                            ground_shoot,
                            next_channel,
                            ammo_ns,
                        )
                    logger.info(
                        f"(he_dca_air) {weapon_descr.namespace}: added air mount "
                        f"-> Ammo_{air_ns} (channel {next_channel})"
                    )
                    continue

                air_shoot = _first_shoot_name(existing_air)
                if air_shoot is None or air_shoot == ground_shoot:
                    next_channel += 1
                    _assign_air_channel(existing_air, next_channel, air_ns)
                    rewired += 1
                    logger.info(
                        f"(he_dca_air) {weapon_descr.namespace}: rewired air mount "
                        f"Ammo_{air_ns} to channel {next_channel}"
                    )
                else:
                    skipped_ok += 1

                if unit_name:
                    # Re-read after possible rewire so depiction metadata matches NDF.
                    air_shoot = _first_shoot_name(existing_air) or (
                        f"WeaponShootData_0_{next_channel}"
                    )
                    air_active_membr = existing_air.v.by_m(
                        "WeaponActiveAndCanShootPropertyName", False
                    )
                    air_active = (
                        strip_quotes(str(air_active_membr.v))
                        if air_active_membr is not None
                        else f"WeaponActiveAndCanShoot_{next_channel}"
                    )
                    channels_by_unit.setdefault(unit_name, []).append(
                        {
                            "ground_shoot": ground_shoot,
                            "air_shoot": air_shoot,
                            "air_active": air_active,
                            "ground_ammo_ns": ammo_ns,
                        }
                    )


    game_db[_CHANNELS_KEY] = channels_by_unit
    logger.info(
        f"(he_dca_air) added {added} air mounts, rewired {rewired}, "
        f"kept {skipped_ok} existing "
        f"({sum(len(v) for v in channels_by_unit.values())} depiction channels)"
    )
