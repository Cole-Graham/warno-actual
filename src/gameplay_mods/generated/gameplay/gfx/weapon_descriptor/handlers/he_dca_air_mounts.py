"""Auto-wire ``Ammo_<x>_AIR`` mounts on every turret carrying a ``DamageFamily_he_dca`` ammo.

Runs as the final step of ``edit_gen_gp_gfx_weapondescriptor`` (after vanilla
renames, new units, unit edits, default salves, and namespace ammo quantity
updates) so the live NDF reflects every prior change. For each
``TMountedWeaponDescriptor`` whose ``Ammunition`` points at a SPAAG ammo
covered by the precomputed ``he_dca_weapons`` map, we clone the mount and
swap the clone's ``Ammunition`` pointer to the matching ``_AIR`` descriptor
(generated by ``apply_he_dca_air_ammo_clones``).

Idempotent: a re-run skips any mount whose ``_AIR`` sibling is already
present in the same ``MountedWeaponDescriptorList``.
"""

import re
from typing import Any, Dict

from src.utils.ndf_utils import is_obj_type, is_valid_turret


_SALVO_SUFFIX_RE = re.compile(r"(_x\d+|_salvolength\d+)$")
_AMMO_PREFIX = "$/GFX/Weapon/Ammo_"


def _extract_ammo_namespace(weapon: Any) -> str | None:
    """Return the ammo namespace (without ``Ammo_`` prefix) or ``None``."""
    ammunition_membr = weapon.v.by_m("Ammunition", False)
    if ammunition_membr is None:
        return None
    raw = str(ammunition_membr.v)
    if _AMMO_PREFIX not in raw:
        return None
    return raw.split(_AMMO_PREFIX, 1)[1].strip()


def _air_namespace(ammo: str) -> str:
    """Append ``_AIR`` to the post-``Ammo_`` namespace, preserving any salvo suffix.

    Matches the cloning rule in ``apply_he_dca_air_ammo_clones`` so the
    swapped pointer always resolves to a real descriptor.
    """
    return f"{ammo}_AIR"


def apply_he_dca_air_mounts(
    source_path: Any,
    logger: Any,
    game_db: Dict[str, Any],
) -> None:
    """Add an air-targeting mount to every turret that carries a ``DamageFamily_he_dca`` ammo."""
    he_dca_weapons = game_db.get("ammunition", {}).get("he_dca_weapons", {})
    if not he_dca_weapons:
        logger.info(
            "(he_dca_air) precomputed he_dca_weapons map is empty; "
            "no air mounts will be added"
        )
        return

    added = 0
    skipped_existing = 0

    for weapon_descr in source_path:
        turret_list_membr = weapon_descr.v.by_m("TurretDescriptorList", False)
        if turret_list_membr is None:
            continue

        for turret in turret_list_membr.v:
            if not is_valid_turret(turret.v):
                continue

            mounted_list_membr = turret.v.by_m("MountedWeaponDescriptorList", False)
            if mounted_list_membr is None:
                continue

            mounts = list(mounted_list_membr.v)
            existing_ammo_namespaces = set()
            ground_mounts = []
            for mount in mounts:
                if not is_obj_type(mount.v, "TMountedWeaponDescriptor"):
                    continue
                ammo_ns = _extract_ammo_namespace(mount)
                if ammo_ns is None:
                    continue
                existing_ammo_namespaces.add(ammo_ns)
                base = _SALVO_SUFFIX_RE.sub("", ammo_ns)
                if base in he_dca_weapons:
                    ground_mounts.append((mount, ammo_ns))

            for mount, ammo_ns in ground_mounts:
                air_ns = _air_namespace(ammo_ns)
                if air_ns in existing_ammo_namespaces:
                    skipped_existing += 1
                    continue

                new_mount = mount.copy()
                new_mount.v.by_m("Ammunition").v = f"{_AMMO_PREFIX}{air_ns}"
                mounted_list_membr.v.add(new_mount)
                existing_ammo_namespaces.add(air_ns)
                added += 1
                logger.info(
                    f"(he_dca_air) {weapon_descr.namespace}: added air mount "
                    f"-> Ammo_{air_ns}"
                )

    logger.info(
        f"(he_dca_air) added {added} air mounts "
        f"(skipped {skipped_existing} existing)"
    )
